package com.sft.hrmsadmin.pojo_calsses;

public class AttendanceSummaryMonths {

    String id;
    String month = "";
    String year = "";

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getMonth() {
        return month;
    }

    public void setMonth(String month) {
        this.month = month;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

}
